import UIKit

extension UIColor {
    class func burple() -> UIColor {
        return #colorLiteral(red: 0.4117647059, green: 0.1333333333, blue: 0.8823529412, alpha: 1)
    }
    
    class func lavender() -> UIColor {
        return #colorLiteral(red: 0.7471337914, green: 0.6092428565, blue: 0.9660431743, alpha: 1)
    }
    
    class func primaryGreen() -> UIColor {
        return #colorLiteral(red: 0.3725490196, green: 0.6941176471, blue: 0.4078431373, alpha: 1)
    }
    
}
